# 1
# С помощью цикла while делите число 100 на 3 столько раз, пока результат не будет меньше 20.

num = 100
while num >= 20:
    num = num / 3
print(num) # 1.111111111111112

# 2
# Пусть у вас есть число 1. С помощью цикла while умножайте его на 2.5 до тех пор, пока результат не станет больше 20.

num = 1
while num <= 20:
    num = num * 2.5
print(num) # 39.0625